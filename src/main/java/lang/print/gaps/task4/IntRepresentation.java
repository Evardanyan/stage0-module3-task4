package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c = 'c';
        char h = 'h';
        char a = 'a';
        char r = 'r';
        int cNum = c;
        int hNum = h;
        int aNum = a;
        int rNum = r;
        System.out.println(cNum + "\n" + hNum + "\n" + aNum + "\n" + rNum);
    }
}
